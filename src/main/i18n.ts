const messages: Record<string, Record<string, string>> = { 'zh-CN': zhCN, en }

let currentLocale = 'zh-CN'

export function setMainLocale(locale: string): void {
  currentLocale = locale === 'en' ? 'en' : 'zh-CN'
}

export function getMainLocale(): string {
  return currentLocale
}

/**
 * Translate a key with optional parameter interpolation.
 * Usage: mt('error.notLoggedIn', { platform: 'B站' })
 */
export function mt(key: string, params?: Record<string, string | number>): string {
  let msg = messages[currentLocale]?.[key] || messages['zh-CN']?.[key] || key
  if (params) {
    for (const [k, v] of Object.entries(params)) {
      msg = msg.replaceAll(`{${k}}`, String(v))
    }
  }
  return msg
}
